class Prioritas2_2 {
    public static void printAsterisk(int n) {

        // TODO: complete this code
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n - i; j++) {
                System.out.print(" ");
            }
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }

    public static void main(String[] args) {
        printAsterisk(5);
    }
}